section .bss
; Small scratch buffer for digit reversal (max 20 digits for 64-bit)
digit_buf: resb 20

section .text
global int_to_ascii
global lut_mul
global fp_div
global abs_val

; int_to_ascii: rax=value, rdi=dest → rdi past end, rax=byte count
int_to_ascii:
    push rbx
    push r12

    mov r12, rdi               ; save dest pointer
    mov rbx, 10
    xor rcx, rcx

    ; Special case: value is 0
    test rax, rax
    jnz .divide_loop
    mov byte [rdi], '0'
    inc rdi
    mov rax, 1
    pop r12
    pop rbx
    ret

.divide_loop:
    xor rdx, rdx
    div rbx                    ; rax = quotient, rdx = remainder
    add dl, '0'
    lea r8, [rel digit_buf]
    mov [r8 + rcx], dl
    inc rcx
    test rax, rax
    jnz .divide_loop

    ; Digits are in digit_buf in reverse order. Write them forward.
    mov rax, rcx
.write_loop:
    dec rcx
    lea r8, [rel digit_buf]
    movzx rdx, byte [r8 + rcx]
    mov [rdi], dl
    inc rdi
    test rcx, rcx
    jnz .write_loop

    pop r12
    pop rbx
    ret

; lut_mul: (Q8 × LUT×1024) >> 10 → Q8
lut_mul:
    imul rdi, rsi
    sar rdi, 10
    mov rax, rdi
    ret

; fp_div: (rdi << 8) / rsi → rax
fp_div:
    mov rax, rdi
    sal rax, 8
    cqo                        ; sign-extend rax into rdx:rax
    idiv rsi
    ret

; abs_val: branchless |rdi| → rax
abs_val:
    mov rax, rdi
    cqo                        ; rdx = 0 if positive, -1 if negative
    xor rax, rdx               ; flip bits if negative
    sub rax, rdx               ; add 1 if negative (two's complement)
    ret
